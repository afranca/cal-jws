package model;

import java.util.Date;
import java.util.List;

public class Calentada extends ModelTemplate{

	private String description;
	private Date date;
	private List calientes;
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public List getCalientes() {
		return calientes;
	}
	public void setCalientes(List calientes) {
		this.calientes = calientes;
	}
	
	
	
	
	
}
